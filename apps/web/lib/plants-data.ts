/** Категории, реально присутствующие в данных (для фильтра — без пустых). */
export const PLANT_CATEGORIES: PlantCategory[] = Array.from(
  new Set(PLANTS.map((p) => p.category)),
);

export function getPlantBySlug(slug: string): Plant | null {
  return PLANTS.find((p) => p.slug === slug) ?? null;
}

/** Похожие растения той же категории (для блока на детальной странице). */
export function getSimilarPlants(plant: Plant, count = 3): Plant[] {
  return PLANTS.filter(
    (p) => p.category === plant.category && p.slug !== plant.slug,
  ).slice(0, count);
}
