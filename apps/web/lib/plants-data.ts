export type PlantCategory =
  | "Хвойные"
  | "Кустарники"
  | "Деревья"
  | "Многолетники"
  | "Живые изгороди";
export type Light = "Солнце" | "Полутень" | "Тень";
export type Care = "Неприхотливое" | "Средний уход" | "Требует внимания";
export type Season = "Весна" | "Лето" | "Осень" | "Зима";

